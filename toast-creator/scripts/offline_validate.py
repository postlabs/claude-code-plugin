"""Standalone static dough validation — no Toast backend required.

    python offline_validate.py <workspace_dir_or_dough_dir>

Runs the vendored dough-engine slice (vendor/engine_core/, regenerated by
sync_engine_core.py) against every dough dir (dough.yaml present) under the
given path. This is STATIC validation only — artifacts stay UNVERIFIED until
an engine-verified publish/bake against a live backend upgrades them.

Per dough:
  1. parse dough.yaml with ruamel;
  2. Dough.model_validate FIRST — the engine's validate_yaml returns [] when
     the dict doesn't parse as a Dough at all, so pydantic parse errors are
     surfaced here as issues (the engine would swallow them);
  3. validate_yaml(dict, store=ShimStore, box=<sibling box.yaml when present>).
     ShimStore answers dough_exists=True for ids found in the SAME workspace;
     any OTHER id also answers True but is reported as a WARNING
     ("external ref unverifiable offline — checked at publish") — offline runs
     never hard-fail on refs we cannot know. ShimStore.get_dough returns None,
     which safely disables the engine's drill-down checks (they need real
     publisher doughs).

Output: one JSON object {engine_core, workspace, reports, provenance, verdict}
with a report per dough {id, dir, issues:[{code,message,hint}], warnings,
verdict}. Exit 0 when every dough passes (warnings allowed), 1 when any
non-warning issue exists, 2 on usage/environment errors.

Provenance: passing artifacts are merged into <workspace>/provenance.yaml as
  artifacts:
    <id>: {validated: static, engine_core: <mojo_rev>, at: <iso>}
Existing entries whose `validated` is anything other than "static"
(engine-verified records, e.g. a real-bake verification) are NEVER clobbered. When the argument
is a single dough dir, the workspace is the nearest ancestor (up to 2 levels)
already holding a provenance.yaml, else the dough dir's parent.

Requires pydantic + ruamel.yaml — run with the embedded Toast Python, or any
interpreter after `uv pip install pydantic ruamel.yaml`.
"""
from __future__ import annotations

import json
import os
import sys
from datetime import datetime, timezone
from pathlib import Path

from _common import PLUGIN_ROOT, utf8_io

utf8_io()

# Keep the vendored tree byte-identical to what sync_engine_core.py wrote —
# no __pycache__ generated inside vendor/ by validation runs.
sys.dont_write_bytecode = True

VENDOR_DIR = PLUGIN_ROOT / "vendor" / "engine_core"

SKIP_DIRS = {".git", "node_modules", "vendor", "__pycache__"}


def fail(msg: str, code: int = 2) -> int:
    print(json.dumps({"error": msg}, ensure_ascii=False))
    return code


if not (VENDOR_DIR / "VERSION.json").is_file():
    sys.exit(fail(
        f"vendored engine slice missing at {VENDOR_DIR} — regenerate it: "
        "python scripts/sync_engine_core.py (env TOAST_REPO = mojo checkout)"))

# Vendor tree FIRST so `app.*` resolves from the slice, never a live checkout.
sys.path.insert(0, str(VENDOR_DIR))

try:
    from pydantic import ValidationError as PydanticValidationError
    from ruamel.yaml import YAML
    from ruamel.yaml.error import YAMLError
except ImportError as e:
    sys.exit(fail(
        f"missing dependency ({e.name}) — offline validation needs pydantic + "
        "ruamel.yaml. Run with the embedded Toast Python (the interpreter peel "
        "uses), or install them: uv pip install pydantic ruamel.yaml"))

import app  # noqa: E402  — must come from the vendored slice

assert os.path.realpath(app.__file__).startswith(os.path.realpath(str(VENDOR_DIR))), (
    f"app resolved outside the vendored slice: {app.__file__}")

from app.doughs.models import Box, Dough  # noqa: E402
from app.doughs.validation.engine import validate_yaml  # noqa: E402

_yaml = YAML()
ENGINE_REV: str = json.loads(
    (VENDOR_DIR / "VERSION.json").read_text(encoding="utf-8"))["mojo_rev"]


class ShimStore:
    """Workspace-scoped stand-in for DoughStore.

    `dough_exists` is True for everything — but ids NOT found in the workspace
    are recorded so the caller can surface them as warnings instead of the
    engine's hard dough_ref_not_found. `get_dough` returns None: the engine's
    drill-down checks need real publisher doughs and skip cleanly on None.
    """

    def __init__(self, known_ids: set[str]) -> None:
        self.known_ids = known_ids
        self.external_refs: list[str] = []

    def dough_exists(self, dough_id: str) -> bool:
        if dough_id not in self.known_ids and dough_id not in self.external_refs:
            self.external_refs.append(dough_id)
        return True

    def get_dough(self, dough_id: str, locale: str | None = None):
        return None


def load_yaml(path: Path):
    with open(path, encoding="utf-8") as f:
        return _yaml.load(f)


def find_dough_dirs(root: Path) -> list[Path]:
    """Every dir under root holding a dough.yaml (covers ./*/doughs/* and
    ./*/kits/*/<flour dirs> plus loose layouts), pruned of infra dirs."""
    found: list[Path] = []
    for cur, dirnames, filenames in os.walk(root):
        dirnames[:] = sorted(
            d for d in dirnames if d not in SKIP_DIRS and not d.startswith("."))
        if "dough.yaml" in filenames:
            found.append(Path(cur))
    return found


def collect_known_ids(dough_dirs: list[Path]) -> set[str]:
    known: set[str] = set()
    for d in dough_dirs:
        try:
            data = load_yaml(d / "dough.yaml")
        except (YAMLError, OSError):
            continue
        if isinstance(data, dict) and isinstance(data.get("id"), str):
            known.add(data["id"])
    return known


def validate_dough_dir(dough_dir: Path, known_ids: set[str]) -> dict:
    issues: list[dict] = []
    warnings: list[str] = []
    dough_id: str | None = None

    try:
        data = load_yaml(dough_dir / "dough.yaml")
    except YAMLError as e:
        data = None
        issues.append({
            "code": "yaml_parse_error",
            "message": f"dough.yaml is not valid YAML: {e}",
            "hint": "fix the YAML syntax — nothing can be validated past this",
        })

    if data is not None and not isinstance(data, dict):
        issues.append({
            "code": "yaml_not_mapping",
            "message": f"dough.yaml parsed as {type(data).__name__}, expected a mapping",
            "hint": "the top level must be the dough mapping (id, inputs, steps/action, return)",
        })
        data = None

    if isinstance(data, dict):
        dough_id = data.get("id") if isinstance(data.get("id"), str) else None

        # The engine swallows parse failures (validate_yaml returns [] when the
        # dict isn't a parseable Dough) — surface pydantic errors ourselves.
        try:
            Dough.model_validate(data)
        except PydanticValidationError as e:
            for err in e.errors():
                loc = ".".join(str(p) for p in err["loc"]) or "<root>"
                issues.append({
                    "code": "dough_parse_error",
                    "message": f"{loc}: {err['msg']}",
                    "hint": "dough.yaml does not parse as a Dough — fix this "
                            "before engine rules can run",
                })

        box = None
        box_path = dough_dir / "box.yaml"
        if box_path.is_file():
            try:
                box_raw = load_yaml(box_path)
                if isinstance(box_raw, dict):
                    box = Box.from_yaml_dict(box_raw)
                else:
                    raise ValueError(
                        f"box.yaml parsed as {type(box_raw).__name__}, expected a mapping")
            except (YAMLError, PydanticValidationError, ValueError) as e:
                issues.append({
                    "code": "box_parse_error",
                    "message": f"box.yaml failed to parse: {e}",
                    "hint": "box.yaml must map locale → {name, about, inputs, outputs}",
                })

        shim = ShimStore(known_ids)
        for issue in validate_yaml(dict(data), store=shim, box=box):
            issues.append({
                "code": issue.code,
                "message": issue.message,
                "hint": issue.hint,
            })
        warnings.extend(
            f"external ref '{ref}' unverifiable offline — checked at publish"
            for ref in shim.external_refs)

    return {
        "id": dough_id or dough_dir.name,
        "dir": str(dough_dir),
        "issues": issues,
        "warnings": warnings,
        "verdict": "pass" if not issues else "fail",
    }


def resolve_workspace(target: Path, is_dough_dir: bool) -> Path:
    if not is_dough_dir:
        return target
    for candidate in (target.parent, target.parent.parent):
        if (candidate / "provenance.yaml").is_file():
            return candidate
    return target.parent


def update_provenance(workspace: Path, passed: list[dict]) -> str | None:
    """Merge static-validation records for passing artifacts; never clobber
    engine-verified entries (validated != "static")."""
    if not passed:
        return None
    path = workspace / "provenance.yaml"
    doc = load_yaml(path) if path.is_file() else None
    if not isinstance(doc, dict):
        doc = {}
    artifacts = doc.get("artifacts")
    if not isinstance(artifacts, dict):
        artifacts = {}
        doc["artifacts"] = artifacts
    now = datetime.now(timezone.utc).isoformat()
    for report in passed:
        existing = artifacts.get(report["id"])
        if isinstance(existing, dict) and existing.get("validated") not in (None, "static"):
            continue  # an engine-verified (e.g. real-bake) record outranks static
        artifacts[report["id"]] = {
            "validated": "static",
            "engine_core": ENGINE_REV,
            "at": now,
        }
    with open(path, "w", encoding="utf-8", newline="\n") as f:
        _yaml.dump(doc, f)
    return str(path)


def main() -> int:
    if len(sys.argv) != 2:
        print(__doc__)
        return 2
    target = Path(sys.argv[1]).resolve()
    if not target.is_dir():
        return fail(f"not a directory: {target}")

    is_dough_dir = (target / "dough.yaml").is_file()
    workspace = resolve_workspace(target, is_dough_dir)
    to_validate = [target] if is_dough_dir else find_dough_dirs(target)
    if not to_validate:
        return fail(f"no dough.yaml found under {target}")

    # Known ids come from the whole workspace, even in single-dough-dir mode,
    # so sibling artifacts authored in the same session resolve as internal.
    known_ids = collect_known_ids(find_dough_dirs(workspace))

    reports = [validate_dough_dir(d, known_ids) for d in to_validate]
    passed = [r for r in reports if r["verdict"] == "pass"]
    provenance_path = update_provenance(workspace, passed)

    all_pass = len(passed) == len(reports)
    print(json.dumps({
        "engine_core": ENGINE_REV,
        "workspace": str(workspace),
        "reports": reports,
        "provenance": provenance_path,
        "verdict": "pass" if all_pass else "fail",
    }, ensure_ascii=False, indent=2))
    return 0 if all_pass else 1


if __name__ == "__main__":
    sys.exit(main())
