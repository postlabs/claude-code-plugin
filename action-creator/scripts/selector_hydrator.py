"""Selector Hydrator — converts target_ref in action YAML to full selectors.

Takes an action YAML where the LLM specified `target_ref` (snapshot ref ID)
instead of writing selectors by hand, and replaces each target_ref with
a multi-strategy selector generated by `generate_selector_set()`.

Usage:
    python selector_hydrator.py \
        --action actions/search_products.yaml \
        --snapshot snapshot.yml \
        [--out actions/search_products.yaml]  # default: overwrite in-place

The snapshot file should be the Playwright MCP snapshot YAML taken from the
same page the action targets.
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path
from typing import Any

import yaml

# Add vendor to path
_PLUGIN_DIR = str(Path(__file__).resolve().parent.parent)
if _PLUGIN_DIR not in sys.path:
    sys.path.insert(0, _PLUGIN_DIR)

from vendor.models import SnapshotNode
from vendor.snapshot_tree import parse_snapshot_tree, walk_tree
from vendor.selector import generate_selector_set


def _find_node_by_ref(tree: SnapshotNode, ref: str) -> SnapshotNode | None:
    """Find a node in the tree by its ref ID."""
    for node in walk_tree(tree):
        if node.ref == ref:
            return node
    return None


def hydrate_step(
    step: dict[str, Any],
    tree: SnapshotNode,
) -> dict[str, Any]:
    """Replace target_ref with generated selector in a single step."""
    ref = step.get("target_ref")
    if not ref:
        return step  # no target_ref → leave unchanged

    node = _find_node_by_ref(tree, str(ref))
    if not node:
        print(f"  WARN: ref={ref} not found in snapshot, skipping")
        return step

    sel_set = generate_selector_set(node, tree)
    if sel_set.is_empty:
        print(f"  WARN: ref={ref} — generate_selector_set returned empty, skipping")
        return step

    # Replace target_ref with selector
    step = dict(step)  # shallow copy
    step["selector"] = sel_set.to_spec()
    del step["target_ref"]

    strategies = [s.strategy for s in sel_set.selectors]
    print(f"  ref={ref} → {len(sel_set.selectors)} strategies: {strategies}")
    return step


def hydrate_action(
    action_def: dict[str, Any],
    tree: SnapshotNode,
) -> dict[str, Any]:
    """Hydrate all steps in an action definition."""
    steps = action_def.get("steps", [])
    hydrated_steps = []
    for i, step in enumerate(steps):
        action_type = step.get("action", "")
        if "target_ref" in step:
            print(f"  step {i + 1} ({action_type}): hydrating...")
            step = hydrate_step(step, tree)
        # Also handle trigger.target_ref for select_custom
        if "trigger" in step and isinstance(step["trigger"], dict):
            trigger = step["trigger"]
            if "target_ref" in trigger:
                ref = trigger["target_ref"]
                node = _find_node_by_ref(tree, str(ref))
                if node:
                    sel_set = generate_selector_set(node, tree)
                    if not sel_set.is_empty:
                        trigger = dict(trigger)
                        trigger["selectors"] = sel_set.to_spec().get("selectors", [])
                        del trigger["target_ref"]
                        step = dict(step)
                        step["trigger"] = trigger
        hydrated_steps.append(step)

    result = dict(action_def)
    result["steps"] = hydrated_steps
    return result


def hydrate_file(
    action_file: Path,
    snapshot_file: Path,
    out_file: Path | None = None,
) -> None:
    """Hydrate an action YAML file using a snapshot."""
    with open(snapshot_file, encoding="utf-8") as f:
        raw_snapshot = f.read()
    tree, _url = parse_snapshot_tree(raw_snapshot)

    with open(action_file, encoding="utf-8") as f:
        raw = yaml.safe_load(f) or {}

    if not raw:
        print(f"SKIP: {action_file.name} — empty file")
        return

    action_name = next(iter(raw))
    action_def = raw[action_name]
    print(f"[{action_name}]")

    # Count target_refs
    ref_count = sum(1 for s in action_def.get("steps", []) if "target_ref" in s)
    if ref_count == 0:
        print(f"  no target_ref found, skipping")
        return

    hydrated = hydrate_action(action_def, tree)
    raw[action_name] = hydrated

    out = out_file or action_file
    with open(out, "w", encoding="utf-8") as f:
        yaml.dump(raw, f, allow_unicode=True, default_flow_style=False, sort_keys=False)
    print(f"  → written to {out}")


def main() -> None:
    parser = argparse.ArgumentParser(description="Selector Hydrator")
    parser.add_argument("--action", type=Path, required=True, help="Action YAML file")
    parser.add_argument("--snapshot", type=Path, required=True, help="Snapshot YAML file")
    parser.add_argument("--out", type=Path, default=None, help="Output file (default: overwrite)")
    args = parser.parse_args()

    hydrate_file(args.action, args.snapshot, args.out)


if __name__ == "__main__":
    main()
